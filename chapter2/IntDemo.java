public class IntDemo
{
    public static void main(String[] vars)
    {
	int aInt = 2147483647;
	byte aByte = 127;
	short aShort = 32767;
	long aLong = 9223372036854775807L;
	System.out.println("max int   "+aInt);
	System.out.println("max byte  "+aByte);
	System.out.println("max short "+aShort);
	System.out.println("max long  "+aLong);
    }
}
